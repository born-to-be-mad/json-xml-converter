class Complex {

    private double real;
    private double imaginary;

    public double getReal() {
        return real;
    }

    public Complex setReal(double real) {
        this.real = real;
        return this;
    }

    public double getImaginary() {
        return imaginary;
    }

    public Complex setImaginary(double imaginary) {
        this.imaginary = imaginary;
        return this;
    }
}
